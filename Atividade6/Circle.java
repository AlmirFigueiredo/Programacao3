package Atividade6;

public class Circle {
    private double xc;
    private double yc;
    private double r;

    public Circle(double xc, double yc, double r) {
        this.xc = xc;
        this.yc = yc;
        this.r = r;
    }
    public double getXc() {
        return xc;
    }
    public double getYc() {
        return yc;
    }
    public double getR() {
        return r;
    }
    
    
}
